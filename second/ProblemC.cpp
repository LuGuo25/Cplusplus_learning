#include <iostream>
using namespace std;

int main() {
	//问题 C: 复仇者联盟
	int num;
	cin >> num;
	switch (num) {
		case 1:
			cout << "\"Failure is the fog through which we glimpse triumph.\"" << endl;
			break;
		case 2:
			cout << "\"In the end, you exhaust everything, go up and down, but still have nothing.\"" << endl;
			break;
		case 3:
			cout << "\"The sun will shine on us again.\"" << endl;
			break;
		case 4:
			cout << "\"Smile, for even in death.\"" << endl;
			break;
	}
	return 0;
}